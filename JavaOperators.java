public class JavaOperators {
    public static void main(String[] args){
        // Java Arithmetic Operators
        int x = 1 + 1;
        int y = x * 2;
        int mySum1 = x + y;
        int mySum2 = x - y;
        int mySum3 = x * y;
        int mySum4 = x / y;
        int mySum5 = x & y;
        int mySum6 = ++x;
        int mySum7 = --x;
        System.out.println(mySum1);
        System.out.println(mySum2);
        System.out.println(mySum3);
        System.out.println(mySum4);
        System.out.println(mySum5);
        System.out.println(mySum6);
        System.out.println(mySum7);
    }
} 
