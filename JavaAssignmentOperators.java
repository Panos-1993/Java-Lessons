public class JavaAssignmentOperators{
    public static void main(String[] args){
        int x = 1;
        x += 5;
        /* 
        List of assignement operators
        =	x = 5	x = 5	
        +=	x += 3	x = x + 3	
        -=	x -= 3	x = x - 3	
        *=	x *= 3	x = x * 3	
        /=	x /= 3	x = x / 3	
        %=	x %= 3	x = x % 3	
        &=	x &= 3	x = x & 3	
        |=	x |= 3	x = x | 3	
        ^=	x ^= 3	x = x ^ 3	
        >>=	x >>= 3	x = x >> 3	
        <<=	x <<= 3	x = x << 3
         */
        System.out.println(x);
    }
}